public class ArithmeticOperations {

    /**
     * Using four variables, three for the values and one for the answer perform +, -, / , *
     */
    public static void main(String[] args) {
        int x = 10;
        int y = 5;
        int z = 2;
        
        int ans = x + y + z;    
        System.out.println(ans);
        
        ans = x-y-z;
        System.out.println(ans);
        
        ans = x/y/z;
        System.out.println(ans);
        
        ans = x*y*z;
        System.out.println(ans);
        
    }
    
}
